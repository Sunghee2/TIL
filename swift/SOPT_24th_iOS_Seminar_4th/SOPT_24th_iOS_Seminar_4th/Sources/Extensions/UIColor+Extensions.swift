import UIKit

extension UIColor {
    @nonobjc class var brownishGrey: UIColor {
        return UIColor(white: 112.0 / 255.0, alpha: 1.0)
    }
    
    @nonobjc class var maize: UIColor {
        return UIColor(red: 245.0 / 255.0, green: 207.0 / 255.0, blue: 69.0 / 255.0, alpha: 1.0)
    }
    
    @nonobjc class var veryLightPink: UIColor {
        return UIColor(white: 199.0 / 255.0, alpha: 1.0)
    }
    
    @nonobjc class var veryLightPinkTwo: UIColor {
        return UIColor(white: 206.0 / 255.0, alpha: 1.0)
    }
    
    @nonobjc class var veryLightPink52: UIColor {
